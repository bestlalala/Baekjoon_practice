package p12865_plain_backpack;

public class Thing {
    int W, V;

    public Thing(int w, int v) {
        W = w;
        V = v;
    }

    public int getW() {
        return W;
    }

    public void setW(int w) {
        W = w;
    }

    public int getV() {
        return V;
    }

    public void setV(int v) {
        V = v;
    }
}
